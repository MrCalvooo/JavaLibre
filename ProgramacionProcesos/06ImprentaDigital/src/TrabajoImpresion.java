public class TrabajoImpresion {
    private int id;
    private int paginas;

    public TrabajoImpresion(int id, int paginas) {
        this.id = id;
        this.paginas = paginas;
    }

    public int getId() {
        return id;
    }

    public int getPaginas() {
        return paginas;
    }
}
